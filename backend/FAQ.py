def load_material_data() -> dict:
    material_map = {}
    tsv_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), "style_materials.tsv")
    if not os.path.exists(tsv_path):
        print("⚠️ style_materials.tsv 파일이 존재하지 않아 자재 정보 병합을 생략합니다.")
        return material_map
        
    try:
        with open(tsv_path, "r", encoding="utf-8") as f:
            reader = csv.reader(f, delimiter="\t")
            header = next(reader)
            for row in reader:
                if not row or len(row) < 10:
                    continue
                style_ko = row[1].strip()
                wallpaper = row[4].strip()
                flooring = row[5].strip()
                difficulty = row[6].strip()
                wallpaper_weather = row[7].strip()
                flooring_weather = row[8].strip()
                precautions = row[9].strip()
                
                material_map[style_ko] = {
                    "wallpaper": wallpaper,
                    "flooring": flooring,
                    "difficulty": difficulty,
                    "wallpaper_weather": wallpaper_weather,
                    "flooring_weather": flooring_weather,
                    "precautions": precautions
                }
        print(f"✅ style_materials.tsv에서 {len(material_map)}개의 자재 매칭 데이터를 로드했습니다.")
    except Exception as e:
        print(f"⚠️ 자재 정보 로드 오류: {e}")
    return material_map

def load_style_documents() -> list:
    url = "https://docs.google.com/spreadsheets/d/1FYOIu_GvwEO-oRIWl5XBlR-PlE13JlYCa4xi6F0PIpM/export?format=csv"
    style_chunks = []
    
    # 로컬 자재 데이터 로드
    material_map = load_material_data()
    
    try:
        response = requests.get(url, timeout=15)
        response.raise_for_status()
        csv_data = response.content.decode('utf-8')
        f = StringIO(csv_data)
        reader = csv.reader(f)
        
        # 첫 번째 라인은 헤더이므로 건너뜀
        header = next(reader)
        
        for row in reader:
            if not row or len(row) < 7:
                continue
            num = row[0].strip()
            style_ko = row[1].strip()
            style_en = row[2].strip()
            feat1 = row[4].strip()
            feat2 = row[5].strip()
            feat3 = row[6].strip()
            
            if not style_ko:
                continue
                
            page_content = f"# 인테리어 스타일: {style_ko} ({style_en})\n"
            if feat1:
                page_content += f"- 특징 1: {feat1}\n"
            if feat2:
                page_content += f"- 특징 2: {feat2}\n"
            if feat3:
                page_content += f"- 특징 3: {feat3}\n"
                
            # 자재 및 시공 데이터 병합
            if style_ko in material_map:
                m = material_map[style_ko]
                page_content += f"- 어울리는 벽지: {m['wallpaper']}\n"
                page_content += f"- 어울리는 바닥재: {m['flooring']}\n"
                page_content += f"- 시공 난이도: {m['difficulty']}\n"
                page_content += f"- 벽지시공 추천날씨: {m['wallpaper_weather']}\n"
                page_content += f"- 바닥시공 추천날씨: {m['flooring_weather']}\n"
                page_content += f"- 날씨별 시공 유의사항: {m['precautions']}\n"
                
            metadata = {
                "source": f"Google Spreadsheet ({num})",
                "category": "interior_style",
                "style_name_ko": style_ko,
                "style_name_en": style_en
            }
            style_chunks.append(Document(page_content=page_content.strip(), metadata=metadata))
            
        print(f"✅ 구글 시트 및 로컬 TSV 결합을 통해 {len(style_chunks)}개의 병합된 스타일 데이터를 생성했습니다.")
    except Exception as e:
        print(f"⚠️ 구글 시트 데이터 로드 실패: {e}")
    return style_chunks

def main():
    api_key = os.getenv("GOOGLE_API_KEY") or os.getenv("GEMINI_API_KEY")
    if not api_key:
        print("❌ 오류: API 키가 존재하지 않습니다.")
        print("프로젝트 루트의 .env 파일에 GEMINI_API_KEY 또는 GOOGLE_API_KEY가 입력되었는지 확인해 주세요.")
        return

    print("🔧 Gemini 임베딩 모델 초기화 중...")
    embeddings = GoogleGenerativeAIEmbeddings(model="models/gemini-embedding-001")

    # ── 구글 스프레드시트 더미 데이터 로드 및 병합 ────────────────────────
    print("📡 구글 스프레드시트 스타일 데이터를 가져오는 중...")
    style_chunks = load_style_documents()
    total_chunks = CHUNKS + style_chunks

    # ── ChromaDB 데이터 로드 및 초기화 (중복 방지) ──────────────────────
    print("ChromaDB 데이터 적재 및 초기화 중...")
    temp_store = Chroma(
        collection_name=config.COLLECTION_KNOWLEDGE,
        embedding_function=embeddings,
        persist_directory=config.DB_DIR
    )
    db_get = temp_store.get()
    if db_get and db_get['ids']:
        print(f"-> 기존 데이터 {len(db_get['ids'])}개를 감지하여 삭제(초기화)를 진행합니다.")
        temp_store.delete(ids=db_get['ids'])

    print(f"💾 ChromaDB 데이터베이스에 {len(total_chunks)}개 청크 적재 중...")
    vector_store = Chroma.from_documents(
        documents=total_chunks,
        embedding=embeddings,
        collection_name=config.COLLECTION_KNOWLEDGE,
        persist_directory=config.DB_DIR,
    )
    print("Chroma DB 영구 적재 완료!\n")

    # ── BM25 색인 파일 사전 빌드 및 직렬화 저장 ───────────────────────
    print("BM25 리트리버 캐시 인덱스 직렬화 빌드 중...")
    bm25_retriever = BM25Retriever.from_documents(total_chunks, preprocess_func=kiwi_tokenize)
    with open(config.BM25_KNOWLEDGE_PATH, "wb") as f:
        pickle.dump(bm25_retriever, f)
    print(f"-> BM25 파일 저장 완료: {config.BM25_KNOWLEDGE_PATH}\n")

