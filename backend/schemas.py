# schemas.py: 프론트엔드와 백엔드가 주고받을 데이터 규격(신청서 양식)을 정의합니다.
# 비유: 우체국에서 택배를 보낼 때 정해진 규격 상자에 담고 운송장을 붙여야 
# 분실 없이 안전하게 배달되는 것과 같습니다.
